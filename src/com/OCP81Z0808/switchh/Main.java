package com.OCP81Z0808.switchh;

public class Main {

	public static void main(String[] args) {

		int marks = 20;
		switch (marks) {
			case 10: System.out.println("10");
				break;
			case 20 : System.out.println("20");
				break;
			case 30 : System.out.println("30");
				break;
			default: System.out.println("default");
				break;

		}

	}
}
